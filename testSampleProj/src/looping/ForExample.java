package looping;

public class ForExample {

	public static void main(String[] args) {
		for (int i = 0; i <= 10; i++) {
			System.out.println(" Value of i is : " + i);
		}
		System.out.println("&&&&&&&&&&&&&&&&&&&");
		for (int i = 10; i >=0; i--) {
			System.out.println(" Value of i is : " + i);
		}
	}

}
